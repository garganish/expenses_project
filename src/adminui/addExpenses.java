    private void btnexpensesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexpensesActionPerformed
        // TODO add your handling code here:

        // Float amount= jtfamount.getText().trim();
        String message = "";
        String description = jtfdescrition.getText().trim();
        String amount = jtfamount.getText().trim();

        boolean allvalid = true;
        if (validate.isEmpty(description)) {
            message = "Plzz fill the subcategoryID";
            allvalid = false;
        }
        if (validate.isEmpty(amount)) {
            message = "Plzz fill the sub_categoryname";
            allvalid = false;
        }
        if (jcbsubinfo.getSelectedIndex() == 0) {
            message = "plzz choose any Category ID";
            allvalid = false;
        }
        if (cdpDob.getSelectedDate() == null) {
            message += "Please Choose Valid Date Of Birth\n\n";
            allvalid = false;
        }
        if (allvalid) {
            expensesinfoTo records = new expensesinfoTo();
            sub_categoryinfoTo sub = (sub_categoryinfoTo) jcbsubinfo.getSelectedItem();
            records.setDescription((description));
            records.setAmount(Float.parseFloat(amount));
            records.setExpensedate(cdpDob.getSelectedDate());
            records.setSubcategoryinfo(sub.getSubcategoryinfo());
            expensesinfoDao actions = new expensesinfoDao();
            if (btnexpenses.getText().contains("Add")) {
                if (actions.insertRecord(records)) {
                    message = "Expenses Details has been saved in the system";
                } else {
                    message = "Insertion Failure" + actions.getErrormessage();
                }
            } else if (btnexpenses.getText().contains("Update")) {
                records.setExpenseid(expenseid);
                if (actions.updateRecord(records)) {
                    message = "Expenses Details has been Updated in the system";
                } else {
                    message = "Update Failure" + actions.getErrormessage();
                }
                operations.openInternalFrame(getDesktopPane(), new viewExpenses());
                dispose();
            } else {
                message = "Invalid Operation Performed";
            }
        }

        JOptionPane.showMessageDialog(this, message);

    }//GEN-LAST:event_btnexpensesActionPerformed

