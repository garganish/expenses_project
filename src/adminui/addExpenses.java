    public addExpenses(expensesinfoTo record) {
        this();
        if (record != null) {
            jtfdescrition.setText((record.getDescription()));
            jtfamount.setText(String.valueOf(record.getAmount()));
            for (int i = 1; i < jcbsubinfo.getItemCount(); i++) {
                sub_categoryinfoTo element = (sub_categoryinfoTo) jcbsubinfo.getItemAt(i);

                if (record.getSubcategoryinfo() == (element.getSubcategoryinfo())) {
                    jcbsubinfo.setSelectedIndex(i);
                    break;
                }
            }
            //for(int i=1;i<cdpDob.)
            cdpDob.setDate(record.getExpensedate());
            //jtfsubid.setEditable(false);//so that our primary key can't be changed.
            btnexpenses.setText("Update Expenses Details");
            setTitle("Update Expenses Details");
            expenseid = record.getExpenseid();
        }
    }

